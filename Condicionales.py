# < Menor

a = 5
b = 6

if a < 5 and b < 6:
    print("Si, la primer condicion es la correcta")

elif a == 5 and b == 6:

    print("Si, la segunda condicion es la correcta")

else:
    print("Ninguna de las dos condiciones es correcta")