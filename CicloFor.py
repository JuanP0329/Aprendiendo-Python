#imprimir numeros con (range) decremento
for numero in range (100, 0, -1):
    print(numero)

#imprimir numeros con (range) incremento
for numero in range (5, 10):
    print(numero)

#para organizar una lista desordenada con de menor a mayor(sorted)

lista = [3,4,1,2,5]
print(lista)

listaOrdenada = sorted(lista)
print(listaOrdenada)

#para organizar una lista desordenada con de menor a mayor(sorted)

lista = [3,4,1,2,5]
print(lista)

listaOrdenada = sorted(lista, reverse=True)
print(listaOrdenada)

